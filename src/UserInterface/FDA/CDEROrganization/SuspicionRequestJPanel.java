    @SuppressWarnings("unchecked")
    
    public void Refresh() {
        int rowCount = suspectedDrugsJTable.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            ((DefaultTableModel) suspectedDrugsJTable.getModel()).removeRow(i);
        }

        ArrayList<WorkRequest> wrList = fdaEnt.getCderOrg().getWorkQueue().getWorkRequestList();
        for (WorkRequest wr : wrList) {
            if (wr instanceof ReportSuspicionWorkRequest) {
                if ((wr.getStatus().equals(WorkRequest.Status.DrugBarred)) && (wr.getStatus().equals(WorkRequest.Status.DrugCleared))) {
                    break;
                } else {
                    Object row[] = new Object[4];
                    row[0] = wr;
                    row[1] = wr.getDrug().getName();
                    row[2] = wr.getDrug().getId();
                    row[3] = wr.getDrug().getManuEnt();

                    ((DefaultTableModel) suspectedDrugsJTable.getModel()).addRow(row);
                }
            }
        }
    }
