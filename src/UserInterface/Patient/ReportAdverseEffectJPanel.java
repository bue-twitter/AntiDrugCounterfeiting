    private void reportJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reportJButtonActionPerformed
        // TODO add your handling code here:
        Drug selectedDrug = (Drug) drugJComboBox.getSelectedItem();
        for (Network n : Business.Business.getInstance().getNetworkDritectory().getNetworkList()) {
            for (Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()) {
                if (e instanceof DispenserEnterprise) {
                    //System.out.println(e);
                    if (e.equals(selectedDrug.getDispEnt())) {

                        AdverseEffectsWorkRequest request = (AdverseEffectsWorkRequest) e.getWorkQueue().newWorkRequest(WorkRequest.Type.AdverseEffectsWorkRequest);
                        request.setPatient(p);
                        request.setStatus(WorkRequest.Status.Sent);
                        request.setDrug(selectedDrug);
                        request.setComments(commentJTextField.getText());
                        request.addIDtoList(request.getId());
 //                       for (PatientOrderItem oi : p.getPatientOrders()) {
 //                           if (oi.getDrug().equals(selectedDrug)) {
 //                               p.getPatientOrders().remove(oi);
 //                           }
 //                       }
                        JOptionPane.showMessageDialog(null, "Complaint for Adverse Effects Sent to Dispenser '"+selectedDrug.getDispEnt()+ "'.");
                    }
                }
            }
        }
    }//GEN-LAST:event_reportJButtonActionPerformed

