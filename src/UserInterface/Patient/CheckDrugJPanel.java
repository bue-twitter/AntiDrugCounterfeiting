    private void checkJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkJButtonActionPerformed
        // TODO add your handling code here:
        Drug d = (Drug)drugJComboBox.getSelectedItem();
        for(Long reqID : d.getRequestIDs()){
            if(reqID.toString().equals(orderIDJTextField.getText())){
                JOptionPane.showMessageDialog(null, "Drug is Legitimate");
                break;
            } else {
                JOptionPane.showMessageDialog(null, "Drug not Legitimate");
                break;
            }
        }
    }//GEN-LAST:event_checkJButtonActionPerformed

